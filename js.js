const input = document.querySelector('input');
const btn = document.querySelector('button');

const photo = document.querySelector('.jpg-flag');
const nameInf = document.querySelector('.country');
const cityInf = document.querySelector('.capitalCity');
const currencyInf = document.querySelector('.currency');
const presidentInf = document.querySelector('.president');



const showCountry = () => {
  const inputVal = countriesInfs.find(el => el.country === input.value)

  if(inputVal){
    nameInf.textContent = inputVal.country
    cityInf.textContent = inputVal.capitalCity
    currencyInf.textContent = inputVal.currency
    presidentInf.textContent = inputVal.president
    photo.setAttribute('src' , inputVal.imgPath)
  }
}

const enterCheck = () => {
  if(event.keyCode === 13){
   showCountry();
  }
 }
 
 showCountry();
 btn.addEventListener('click' , showCountry)
 input.addEventListener('keyup', enterCheck)